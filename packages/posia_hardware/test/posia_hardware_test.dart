import 'dart:io';
import 'dart:typed_data';

import 'package:flutter_test/flutter_test.dart';
import 'package:posia_hardware/posia_hardware.dart';

Uint8List _pngMinimo() {
	// PNG 1x1 blanco valido para pruebas de archivo.
	return Uint8List.fromList([
		0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A,
		0x00, 0x00, 0x00, 0x0D, 0x49, 0x48, 0x44, 0x52,
		0x00, 0x00, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01,
		0x08, 0x06, 0x00, 0x00, 0x00, 0x1F, 0x15, 0xC4,
		0x89, 0x00, 0x00, 0x00, 0x0A, 0x49, 0x44, 0x41,
		0x54, 0x78, 0x9C, 0x63, 0x00, 0x01, 0x00, 0x00,
		0x05, 0x00, 0x01, 0x0D, 0x0A, 0x2D, 0xB4, 0x00,
		0x00, 0x00, 0x00, 0x49, 0x45, 0x4E, 0x44, 0xAE,
		0x42, 0x60, 0x82,
	]);
}

void main() {
	test('ImpresoraConfigurable modo archivo guarda ticket PNG', () async {
		final directorio = Directory.systemTemp.createTempSync('posia_ticket_test').path;
		final impresora = ImpresoraConfigurable(
			modo: ModoImpresora.archivo,
			hostRed: '',
			directorioArchivo: directorio,
		);
		await impresora.imprimirTicket(imagenTicketPng: _pngMinimo());
		final archivos = Directory(directorio)
			.listSync()
			.whereType<File>()
			.where((f) => f.path.endsWith('.png'));
		expect(archivos.isNotEmpty, isTrue);
	});
}
